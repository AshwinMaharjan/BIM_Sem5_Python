import numpy as np

# All zeros
zero_array = np.zeros((5, 5))
print("Zero Array:\n", zero_array)

# Identity matrix (diagonal elements 1)
identity_array = np.eye(5)
print("Identity Matrix:\n", identity_array)

# All ones
ones_array = np.ones((5, 5))
print("Ones Array:\n", ones_array)

